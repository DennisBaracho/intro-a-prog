"""Implemente e teste um programa que leia 3 números reais (ponto flutuante) e imprima a sua média aritmética."""

num1 = float(input("Digite o primeiro numero inteiro: "))
num2 = float(input("Digite o segundo numero inteiro: "))
num3 = float(input("Digite o terceiro numero inteiro: "))

media = (num1 + num2 + num3)/3

print(f"\nA media dos tres numeros eh: {media}")